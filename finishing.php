		<h2>Penyelesaian / Finishing</h2>
		<p>Finishing merupakan proses akhir dari  produksi pakaian , proses finishing 
		diantaranya : Lubang kancing (button hole), Pemasangan kancing (Buttoning), 
		Pembersihan benang  (Triming), Inspection, Repairing, Pressing and ironing, Size 
		sorting, Inserting polibag, assorting Sz/Carton  serta packing.</p>
		<ol type="1">
		<li>Lubang kancing (button hole) <p>
		Membuat lubang kancing pada bagian yang telah diberi tanda , dengan menggunakan mesin button hole.  </p>
		<li>Memasang kancing (Buttoning) <p>
		Merupakan tahap memasang kancing pada bagian yang telah diberi tanda 
		untuk memasang kancing.</p>

		<li>Pembersihan benang (Triming)<p> 
		Pada bagian ini , merupakan tahap perbersihan sisa- sisa benang yang belum 
		bersih dari bagian sewing,</p>

		<li>Inspection/QC
		<p>Tugas pokok pada bagian ini yaitu memeriksa  cacat atau kesalahan yang 
		mungkin ditemukan pada setiap produk pakaian, disamping itu juga  
		mengecek standar ukuran,  pada setiap cacat/kesalahan diberi tanda sehingga 
		memudahkan untuk memperbaiki atau mengelompokkan setiap jenis 
		kesalahan , lalu dibawa ke bagian repairing. Apabila pakaian tersebut tidak 
		cacat, langsung dibawa ke bagian pressing dan  ironing </p>

		<li>Repairing 
		<p>Pada bagian repairing disediakan  beberapa mesin jahit dan peralatan 
		menjahit lainnya  seperti gunting, jarum, sikat, alat pembersih minyak/kotoran
		dan sebagainya. Peralatan tersebut digunakan untuk memperbaiki  pakaian 
		yang  cacat dari bagian  inspection .</p>

		<li>Pressing and ironing
		<p>Pada bagian ini pakaian disetrika dengan rapih dan cepat. Pada umumnya 
		setrika yang digunakan adalah setrika uap.<br> Beberapa hal yang perlu 
		diperhatikan agar pakaian tidak rusak  pada proses pressing and ironing 
		adalah : <ol type="1">
		<li>Hindarkan mesin pressing yang terlalu panas agar pakaian tidak berkilau. 
		<li>Disetrika dengan rapih supaya tidak kusut.  
		<li>Dilipat dengan baik agar tidak terjadi lipatan lain  yang tidak diinginkan . 
		</ol></p>

		<li>Size sorting

		<p>Memilih pakaian sesuai dengan ukuran yang telah ditentukan , misalnya 
		lingkar pinggang, lingkar panggul sesuai atau tidak dengan ukuran standar.

		<li>Inserting polibag 
		<p>Memasang atau menyisipkan macam-macam asesoris, label ,merk dan 
		lainnya. Pemasangan label sesuai dengan pesanan buyer atau  perusahaan itu sendiri. </p>

		<li>Assorting Sz/Carton
		<p>Memilih/ mengelompokkan pakaian berdasarkan model, ukuran serta label atau merk .</p>

		<li>Packing 
		<p>Setelah dari bagian assorting size pakaian dikemas , bentuk kemasan dapat 
		ditentukan oleh perusahaan itu sendiri atau berdasarkan permintaan  buyer.</p>
		
		
