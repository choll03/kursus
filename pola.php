		<h2>Cara Membuat Pola Dasar Pakaian Wanita</h2>
		<p>
		Berikut cara membuat pola dasar pakaian atau baju wanita dewasa.Pakai wanita dewasa memiliki pola yang sederhana sebenarnya.<br>
		Hanya saja tergantung model baju atau pakaian wanita yang bagaimana yang mau kita buat pola nya.
		Nah di sini kali ini saya akan akan share cara membuat pola pakaian atau baju wanita yang sangat sederhana.
		Dan pola ini dianjurkan untuk pemula dalam belajar menjahit pakaian.
		Seperti yang telah kita ketahui untuk menjahit sebuah pakaian atau baju supaya memngasilkan hasil yang bagus terlebih dahulu kita 
		membuat pola dengan cara menggambarnya. Berikut di bawah ini langkah - langkah membuat pola pakaian atau baju wanita yang sangat sederhana dan mudah di pahami.</P>
		POLA DASAR BAJU WANITA DEWASA<br><br>
		SISTEM SEDERHANA<br>
		
		<p>Bagi anda yang ingin belajar untuk menjahit baju sendiri dan tidak punya waktu untuk mengikuti kursus menjahit, maka berikut ini adalah langkah-langkah untuk menggambar pola dasar pakaian wanita sistem sederhana yang dapat anda gunakan. Untuk para pemula dalam bidang jahit menjahit sistem membuat pola dasar sederhana ini akan memudahkan anda dalam mempelajari langkah demi langkah pembuatannya.
		Gambar dibawah ini menggunakan ukuran badan saya, 
		sementara jika ukuran badan anda lebih besar atau lebih kecil dari ini maka anda tinggal menyesuaikan sesuai ukuran badan anda.</p>
		
		UKURAN:<ol type="1">
