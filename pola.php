		CARA MENGAMBIL UKURAN :
		<ol type="1">
		<li>Lingkar Pinggang, diukur pas sekeliling pinggang<br>
		<li>Lingkar pesak, diukur dari batas pinggang belakang, melalui selangkangan menuju garis pinggang bagian muka.<br>
		<li>Tinggi duduk, diukur dari pinggang sampai batas panggul terbesar  pada bagian belakang (dalam posisi duduk)<br>
		<li>Lingkar Panggul, diukur melingkar pada pinggul yang paling lebar  secara horizontal ditambah 4 cm.<br>
		<li>Panjang celana, diukur dari pinggang sampai batas mata kaki  (sesuai dengan model)<br>
		<li>Lingkar ujung kaki, Diukur sekeliling ujung kaki celana sesuai dengan ukuran yang  diinginkan<br>
		<li>Lingkar paha, diukur sekeliling paha terbesar<br><br>

		</ol>
		
		KETERANGAN  POLA CELANA BAGIAN MUKA <br>
		A - B = panjang celana.1<br>
		A - C = 1 /3 lingkar pesak dibagi 3 ditambah 4 cm. <br>
		C - D = C - E - ¼ lingkar pinggang ditambah 4 cm. <br>
		E - D1 = 4 cm tarik garis lurus sampai garis pinggang namakan titik H. <br>
		H - G = lingkar pinggang dibagi 4 ditambah 2 cm. <br>
		A - F = panjang lutut. <br>
		F - F1 = F - F2 = ½ lingkar lutut.<br> 
		B - B1 = B - B2 = ½ lingkar kaki celana.<br> 
		G - I = 3 cm. <br>
		G - j = 12 cm. <br>
		Hubungkan I dengan j seperti gambar saku  sisi celana. <br>
		Hubungkan H dengan E seperti gambar ( pesak celana bagian muka).<br> 
		Hubungkan E dengan F2 terus ke titik B2, seperti gambar (garis sisi celana).<br> 
		Hubungkan G dengan D membentuk garis panggul, terus ke titik B1 melalui titik F1 seperti gambar (sisi celana).<br><br><span id="dots">...</span><span id="more">

