		KETERANGAN POLA BADAN MUKA:<br><br>

		A – B = 1/6 Lingkar leher + 2 cm<br>
		B – C = Panjang Muka<br>
		C – D = A – E = ¼ Lingkar badan + 1cm<br>
		A – A1 = 1/6 Lingkar leher + 0,5 cm<br>
		A1 – A2 = Panjang bahu<br>
		A2 – A3 = turun 4 cm<br>
		B – B1 = 5 cm<br>
		B1 – B2 = ½ Lebar muka<br>
		C – C1 = ¼ Lingkar pinggang + 1 + 3 cm<br>
		C – C2 =1/10 Lingkar pinggang + 1 cm<br>
		C2 – CC3 = 3 cm<br>
