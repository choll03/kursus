		TIPS BAGI YANG MALAS MEMBUAT POLA :<br><br>

		Dan jika dengan sistem sederhana anda masih juga males untuk membuat pola dasar baju sendiri, maka berikut trik yang bisa gunakan :<br>
		<ol type="1">
		<li>Carilah baju anda yang sudah tidak terpakai lagi tapi masih pas dibadan, mungkin baju ini tidak anda pakai lagi karena terkena noda atau warnanya yang sudah pudar 
		<li>Ambil silet dan bukalah jahitannya, tapi jangan semuanya melainkan hanya separuh saja, ya separuh bagian muka dan separuh bagian belakang, satu lengan dan kerah. Sedangkan separuh bagian yang lain biarkan masih terjahit seperti aslinya, gunanya adalah untuk referensi kita pada saat kita menjahit pakian yang baru. Jika kita mengalami kebingungan bagian-bagian mana yang harus kita jahit serta bagaimana cara menjahit sakunya atau lengannya, tinggal kita lihat deh.
		<li>Seterika potongan potongan kain yang sudah terlepas jahitannya tadi sampai bagian bekas jahitan rata dan rapi.
 
		<li>Tempelkan potongan kain yang sudah diseterika rapi diatas kertas koran dan gambarlah koran tadi mengikuti pinggir potongan kain dengan spidol. 
		</ol>Nah Jadi deh pola baju yang sudah siap untuk kita gunakan.<br><br>
		
		POLA DASAR LENGAN<br><br>
		<img src="D:\Kuliah\Tugasbesar\lengan.jpg" width="433" height="342"id="pola"><br>
		
		Ukuran Yang Diperlukan<ol type="1">
		<li>Lingkar kerung lengan = 40cm (diukur dari pola badan)<br>
		<li>Tinggi puncak lengan = 12 cm<br> 
		<li>Panjang lengan = 24 cm 
		</ol>
		Keterangan pola lengan<br><br>

		Menggambar pola lengan dimulai dai titik A yang merupakan puncak lengan.<br><br>

		A - B = panjang lengan.<br>
		A - C = ukuran tinggi puncak lengan, buat garis sampai ke titik D dan E, 
		setelah diukur dari titik A ½ lingkar kerung lengan yang ukurannya bertemu dengan garis dari tititk C.<br><br>

		Buat garis putus-putus (garis bantu) dari A ke D dan dari A ke E.<br>
		Garis bantu dari A ke D dan A ke E dibagi tiga. 1/3 dari A ke D<br>
		diberi titik A1 dan dari A ke E dinamakan titik A2.<br><br>

		A1 - A4 = A2 - A3 = 1,5 cm.<br>
		Titik D1 = 1/3 D - A<br>
		D ke D1 dibagi dua dinamakan titik D2.<br>
		D2 - D3 = 0,5 cm.<br><br>

		Hubungkan A dengan A4 dengan D1, D3 dan D seperti gambar (lingkar kerung lengan bagian muka).<br>
		Hubungkan A dengan A3 dan E seperti gambar (lingkar kerung lengan bagian belakang).<br><br>

		G - G1 = E1 - E2 = 1,5 cm.<br>

		Hubungkan E dengan E2 (sisi lengan bagian belakang), dan D dengan G seperti gambar (sisi lengan bagian muka)<br><br>

		POLA DASAR ROK<br><br>
		<img src="D:\Kuliah\Tugasbesar\rok.jpg" width="433" height="500" id="pola"><br>
		Keterangan pola rok muka<br><br>

		Menggambar pola rok dimulai dari titik A.<br>
		A - B = panjang rok.<br>
		A - C = tinggi panggul.<br>
		A - A1 = ¼ lingkar pinggang ditambah 4 cm ( 3 cm untuk besar lipit kup, 1 cm untuk membedakan ukuran pola muka degan pola belakang).<br>
		A1 - A2 = 1,5 cm.<br>
		Hubungkan A dengan A1 seperti gambar (garis pinggang).<br>
		A - D = 1/10 lingkar pinggang.<br>
		D - D1 = 3 cm.<br>
		Pada garis tengah antara D dan D1 dibuat garis lurus sampai batas garis C dengan C1(garis panggul).<br>
		D - D1 = 12 cm.<br>
		C - C1 = ¼ lingkar panggul ditambah 1 cm.<br>
		B - B1 = C - C1.<br>
		B1 - B2 = 3 cm.<br>
		B2 - B3 = 1,5 cm.<br><br>
		Hubungkan A1 dengan C1 membentuk garis pinggul dan dari C1 ke B3. <br>
		Hubungkan B dengan B3 seperti gambar (garis bawah rok).<br><br>
		Keterangan pola rok belakang<p>
		Menggambar pola rok bagian belakang sama dengan cara meggambar pola rok bagian muka. 
		Bedanya hanya terletak pada ukuran lingkar pinggang dan lingkar panggul. <br>
		Ukuran lingkar pinggang dan ukuran lingkar panggul pola bagian muka lebih besar 2 cm dari pada pola bagian belakang.
		Tetapi bentuk garis sisi, garis pinggang dan garis bawah rok sama dengan pola rok bagian muka.
		Untuk itu maka pola rok bagian belakang dibuat dari pola rok bagian muka. <br>
		Untuk membedakannya cukup dengan memindahkan garis tengah muka sebesar 2 cm dengan cara mengukur dari A ke E sama dengan dari B ke F yaitu 2 cm,
		hubungkan titik E dengan F dengan garis lurus (garis tengah belakang).</p>	

		<p>Jika ingin memiliki pola bagian muka dan pola bagian belakang pada kertas yang berbeda, 
		sebaiknya salah satu dari pola rok dipindahkan. Sebaiknya pola yang dipindahkan itu adalah pola bagian belakang, 
		dengan demikian pada pola rok bagian muka juga terdapat pola bagian belakang.<br>
		Didalam memindahkan pola perlu diperhatikan garis tengah belakang pola mesti dalam posisi lurus, 
		garis pinggang dan garis sisi rok bentuknya mesti sama dengan yang asli.</p>
				POLA DASAR CELANA<br><br>
