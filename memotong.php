		<h2>TEKNIK MEMOTONG BAHAN</h2>
	
		<p>Memotong bahan adalah termasuk pekerjaan yang harus dilaksanakan dengan teliti dan seksama. Hasil potongan bahan yang kurang baik akan mempengaruhi hasil akhir dari proses pembuatan busana.</p>

		<ol type="1">
		<li>Syarat Ruang Kerja dalam memotong bahan :<ol type="A">
			<li>Memiliki penerangan yang baik, tidak terhadang oleh furnitur atau barang lainnya
			<li>Sirkulasi udara yang cukup nyaman
			<li>Ukuran meja potong sesuai standart, permukaan meja rata dan datar
			<li>Gambar tanda bahaya/peringatan diletakkan pada tempat yang terlihat<br><br>
			</ol>
		<li>Alat-alat / Perlengkapan memotong <br><br><ol type="A">
			<li>Pita ukur, digunakan dalam setiap langkah proses pembuatan suatu busana mulai dari mengambil ukuran hingga
			<li>Jarum pentul, digunakan untuk menyematkan pola diatas bahan. Ada beberapa jenis jarum pentul:<ol type="i">
				<li>Kecil, seluruhnya dari logam baik kepala maupun ujung jarum. Biasanya digunakan untuk pekerjaan mendisplay busana di etalase
				<li>Sedang, badan sampai ujung jarum dibuat dari logam, kepala jarum dari plastik warna-warni, digunakan untuk hiasan display busana.
				<li>Panjang, lebih panjang, kecil dan runcing. Badan sampai ujung jarum dari logam, kepala jarum dari plastik warna-warni, digunakan untuk menyemat pola ke bahan pada proses memotong bahan atau menyemat bahan ke bahan pada proses menjahit.
				<li>Panjang dan besar, badan dan ujung jarum dari logam, kepala jarum dari plastik warna-warni, digunakan untuk hiasan.</ol>
		<li>Pemberat untuk membantu menahan kedudukan bahan pada waktu memotong agar tidak bergeser. Biasanya dipakai untuk bahan yang licin atau memotong bahan dalam jumlah banyak.
		<li>Gunting, digunakan untuk memotong bahan, menggunting benang, pada waktu proses pembuatan busana. Macam-macam gunting adalah:<ul type="circle">
		<li>Gunting Bahan
		<li>Gunting Kertas
		<li>Gunting Benang/gunting tiras
		<li>Gunting zig-zag</ul><ol type="i">
		<li>Alat pemberi tanda<ul>
		<li>Kapur Jahit
		<li>Rader dan Karbon Jahit
		<li>Jarum jahit dan benang jahit</ul></ol></ol><br><br>
		<li>Menyiapkan bahan<br><br>Bahan yang akan dipotong harus disiapkan dengan cermat untuk mencegah terjadinya kegagalan produksi (pembuatan busana).<ol type="A">
		<li>Kualitas bahan harus bagus, baik struktur tenunannya maupun penyempurnaan akhirnya.<ul>
		<li>Jika bahan dipotong tidak lurus pada saat membeli bahan, maka bahan harus diluruskan dengan cara memotong lurus menurut arah benang pakan yang ditarik.
		<br><img src="kualitasbahan.jpg"><br>
		<li>Jika bahan yang akan dipotong diperkirakan menyusut maka bahan tersebut harus dicuci terlebih dahulu.
		<li>Jika bahan yang akan dipotong kusut, maka harus disetrika terlebih dahulu.
		<li>Bahan ditarik keempat arah agar lurus<br><img src="bahanditarik.jpg">

		<li>Bahan dilipat dua pada lebar bahan
		<li>Bagian buruk kain berada disebelah luar<ol type="i">
		<li>Kuantitas bahan harus sesuai dengan jumlah kebutuhan atau benda yang akan dibuat (berdasarkan rancangan bahan yang telah dibuat) sehingga kemungkinan kekurangan atau kelebihan bahan tidak terjadi.
		</ol></ol><br>
		
		<li>Menyiapkan pola:<ol type="A"><br>
		<li>Memeriksa kelengkapan jumlah pola.
		<li>Memeriksa kelengkapan tanda-tanda pola, agar tidak terjadi kesalahan pada waktu meletakkan pola yang berakibat kesalahan pada waktu memotong bahan.
		<li>Sudahkah pola diberi kampuh</ol><br>
		<li>Memeriksa bahan <br><br>

		Motif bahan<ol type="1"><br>

		<li>Arah motif bahan:<ol>
		<li>Satu arah, pola diletakkan searah dengan motif
		<li>Dua arah, pola dapat diletakkan bersilang (menghadap 2 arah yang berlawanan)</ol>
		<li>Motif garis/kotak</ol><br>
		Motif bahan harus ditemukan kemudian disemat dengan jarum pentul pada beberapa tempat agar bahan (motif) tidak bergeser. Pola diletakkan pada bahan dan disemat dengan jarum pentul dijahit motif garis dan kotak terus bersambung.
		<br><br>
		Tekstur bahan<br><br>

		Bahan tekstil mempunyai tekstur yang berbeda-beda, karena proses pembuatan dan penyempurnaan yang berbeda-beda pula.
		<ol type="1">
		<li>Pada bahan yang berbulu, tenunan lepas dan berkilau pola diletakkan denagn posisi satu arah.
		<li>Pada bahan yang bertekstur kusam, kasar, dan ditenun dengan silang polos, maka pola dapat diletakkan dengan dua arah.
		<li>Pada bahan yang tipis (tembus pandang) penambahan kampuh cukup 1 cm dan untuk kelim dibuat dua kali lebar kelim.</ol><br>
		<br>
		<li>Meletakkan pola di atas bahan<ol type="1"><br>

		<li>Pola-pola yang besar diletakkan terlebih dahulu, biasanya pola besar diletakkan disudut bahan setelah dilipat dua. Baru kemudian pola-pola yang kecil (tata letak pola sesuai dengan rancangan bahan yang sudah dibuat).
		<li>Setelah yakin tidak akan ada perubahan, pola disemat dengan jarum pentul. Arah kepala jarum pentul kedalam sedangkan ujungnya menghadap keluar.
		</ol><br>
		<li>Memotong bahan<br><br><span id="dots">...</span><span id="more">

		Setelah semua pola disemat diatas bahan dengan benar, bahan dipotong dengan menggunakan gunting bahan. Pada waktu memotong bahan:
		<ol type="1">
		<li>Lubang kecil pada gunting berada diposisi atas ditahan oleh ibu jari sedangkan lubang yang lebih besar berada dibawah, ditahan oleh empat jari lainnya.
		<li>Posisi tangan kiri berada diatas bahan, menekan agar bahan tidak terangkat, tangan kanan memegang gunting dengan banar
		<li>Gunting dibuka lebar-lebar pada tiap kali memotong, agar tepi bahan yang digunting rata.
		<li>Bahan tidak boleh diangkat atau diputar posisinya pada waktu dipotong
		<li>Yang harus diperhatikan adalah hasil potongan bahan tidak boleh terputus-putus.<br><br>
		
		Demikian beberapa hal yang dilakukan dalam proses pemotongan bahan, semoga bermanfaat.<br>
		Sumber:https://ontbpwjt.wordpress.com/2017/04/01/teknik-memotong-bahan/<br></ol></ol></span></p>
