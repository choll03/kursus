		<p>Naahhh ... setelah kita mengetahui pengertian menjahit, selanjutkan kita akan belajar mengenai " Teknik Dasar Menjahit ".<br>
		Karena dalam dunia busana apabila seseorang ingin menekuni bidang ini maka ia harus bisa menggunakan mesin jahit beserta alat-alat yang digunakannya.
		Belajar menjahit sebenarnya diawali dengan niat dan bertanya dengan orang yang ahli dalam bidang busana khususnya menjahit untuk membantu Anda belajar menjahit.
		</p>
		Langsung saja saya akan memberikan 3 dasar menjahit, diantaranya : <br><br>
		<h3>A. Melakukan Pengukuran Badan </h3>
		<p>Pengukuran badan adalah hal dasar  yang perlu dipelajari sebelum masuk untuk memebuat pola.<br>
		Cara melakukan pengukuran badan dapat dikatakan cukup mudah. Model yang akan di jadikan tolak ukur harus berdiri dengan tegak.<br>
		Tujuannya adalah untuk mempermudah dalam pengukuran sehingga setiap ukuran yang diambil tepat dan akurat sesuai dengan ukuran tubuh model.</p>
		Adapun bagian-bagian tubuh yang diukur diantaranya adalah:
		<ol>
		<li>Pengukuran Lingkar Leher(LL)<p>
		Pengukuran pada lingkar leher dilakukan pada sekeliling leher bagian bawah dengan cara meletakkan jari telunjuk pada tekuk leher atau diukur lalu ditambah 1 cm.
		</p>
		<li>Pengukuran Lingkar Badan(LB)<p>
		Pengukuran lingkar badan dilakukan pada bagian atas badan. <br>Tepatnya yaitu pada bagian dada, dengan mengambil bagian badan yang paling besar sebagai tolak ukur. <br>Ukuran lingkar badan yang didapatkan dari pengukuran lingkar badan model kemudian diberi tambahan empat cm atau sebesar empat ruas jari.
		</p>
		<li>Pengukuran Lingkar Pinggang(LP)<p>
		Seperti namanya, pengukuran ini akan menghitung seberapa besar diameter pinggang.</p>

		<li>Pengukuran Tinggi Panggul(TPa)<p>
		Pengukuran ini dilakukan pada bagian bawah pinggang sampai pada batas panggul.</p>

		<li>Pengukuran Lingakar Panggul(LP)<p>
		Pengukuran pada bagian lingkar panggul dilakukan pada sekeliling panggul dengan menggunakan bagian terbesar dari panggul sebagai tolak ukur urutan. <br>Ukuran lingkar panggul yang didapatkan kemudian ditambah lagi empat cm atau seukuran dengan telapak tangan.
		</p>
		<li>Mengukur Panjang Sisi(PS)<p>
		Pengukuran panjang sisi badan dilakukan dengan cara menyelakan penggaris pada bagian bawah ketiak.<br>
		Selanjutnya dilakukan pengukuran yang dimulai dari batas penggaris ke bawah sampai mencapai bagian bawah pinggang, kemudian dikurangi sebanyak 2-3 cm.
		</p>
		<li>Panjang Muka(PM)<p>
		Pengukuran panjang pada sisi bagian muka dilakukan dengan pengukuran yang dimulai dari lekuk leher tepat di bagian tengah kebawah hingga mencapai bagian pinggang bagian bawah. <br>Hasil pengukurannya kemudian dikurangi 2-3 cm.
		</p>
		<li>Lebar Muka(LM)<p>
		Pengukuran lebar pada sisi bagian depan atau lebar muka diukur dengan cara mengambil patokan awal 5cm dibawah lekuk leher pada bagian tengah. Kemudian dilakukan pengukuran secara mendatar yang dimulai dari lengan kiri hingga batas lengan kanan.
		</p>
		<li>Pengukuran Tinggi Dada(TD)<p>
		Tinggi dada diukur dengan cara mengambil tolak ukur yang dimulai dari bagian pinggang kemudian ditarik ke atas hingga mencapai puncak buah dada.
		</p>
		<li>Pengukuran Lebar Bahu(LB)<p>
		Lebar bahu di ukur dengan cara menempatkan titik ukur awal di lekuk leher atau pada bagian tertinggi dari bahu kemudian di tarik ke bagian yang paling rendah atau pada bagian ujung bahu.
		</p>
		<li>Ukuran Uji(UU)<p>
		Ukuran Uji atau ukuran control, diukur dari tengah muka dibawah ban serong melalui puncak dada ke puncak lengan terus serong ke belakang sampai tengah belakang pada bawah ban.
		</p>
		<li>Pengukuran Rok<p>
		Cara mengukur rok muka, belakang, dan sisi, dilakukan dengan satu cara yang sama.<br>
		untuk membuat ukuran rok dapat dilakukan dengan pengukuran yang dimulai dari pingga kebawah sesuai panjang yang dikehendaki.</p>

		<li>Membuat Ukuran Lingakar Lubang Lengan(LLL)<p>
		Pengukuran lingkar lubang lengan dilakukan dengan mengukur langsung sekeliling lengan yang kemudian di berikan tambahan ukuran sepanjang 4 cm.</p>

		<li>Menetukan Panjang Lengan Pendek(PLPd)<p>
		Penentuan panjang lengan pendek dilakukan dengan melakukan pengukuran yang dimulai dari lengan bagian atas atau pada puncak lengan kemudian ditarik kebawah hingga mendekati siku. <br>Tepatnya kurang lebih 3 cm diatas siku.
		</p>
		<li>Pengukuran panjang lengan panjang (PLP)<p>
		Dilakukan dengan pengukuran dimulai dari engan paling atas ke bawah hingga mencapai pergelangan tangan.</p>

		<li>Lingkar lengan panjang(LLP)<p>
		Pengukuran pergelangan untuk lengan panjang sama seperti pada pengukuran lingkar lengan bagian atas. Caranya yaitu mengukur lingkaran pergelangan yang kemudian ditambah 3cm.
		</ol></p>
		
		<h3>B. Menyiapkan Alat dan Bahan Membuat Pola</h3>
		Alat dan Bahan Untuk Membuat Pola<br><br>
		Adapun alat-alat yang dibutuhkan dalam belajar menjahit diantaranya adalah:

		<ol><li>Meteran<p>

		Meteran digunakan untuk melakukan pengukuran badan sebagai acuan dalam cara membuat pola baju.<br> Gunakan meteran yang menggunakan satuan ukuran inci dan sentimeter.</p>

		<li>Buku Pola<p>

		Buku pola adalah buku yang khusus digunakan untuk membuat pola. <br>Buku yang berukuran folio ini berisi lebaran-lembaran yang bergaris-garis untuk mencatat ukuran dan keterangan, selang seling dan juga polos yang bertujuan untuk media menggambar pola yang dibuat dalam ukuran skala.</p>

		<li>Skala<p>

		Skala adalah alat yang digunakan sebagai pembanding yang digunakan untuk mengukur pola berdasarkan gambar yang telah dibuat pada buku pola.<br>
		Skala terbuat dari bahan karton yang berbentuk penggaris dengan berbagai macam ukuran skala, diantarnya: 1:2, 1:3, 1:4, 1:6, dan yang terakhir 1:8.</p>

		<li>Pensil<p>

		Terdapat berbagai macam warna pensil yang digunakan dalam cara membuat pola baju. Setiap warna memiliki fungsi yang berbeda-beda. Diantaranya:<br></p>
		<ol>
		<li>pensi hitam dipakai untuk menggambarkan garis pola
		<li>Pensil berwarna merah digunakan untuk menggambar pola jadi untuk bagian muka.
		<li>pensil berwarna biru digunakan untuk membuat pola jadi untuk pola bagian belakang.</ol><br>

		<li>penggaris<p>

		Terdapat beberapa macam penggaris yang digunakan dalam menjahit. Diantanya adalah penggaris lurus, penggaris siku, penggaris yang berbentuk panggul,lengan, serta leher.</p>

		<li>Karet penghapus<p>

		karet penghapus digunakan untuk menghapus gambar/pola apabila terjadi kesalahan dalam pembuatannya.</p>

		<li>Kertas sampul<p>

		Kertas sampul digunakan untuk membuat rancangan dan meletakkan pola gambar ukuran yang sesungguhnya yang akan di pindahkan ke kain.</p>

		<li>Gunting<p>

		Gunting digunakan untuk membuat atau memotong pola yang telah dibuat, baik pola kecil maupun pola besar.</p></ol>

