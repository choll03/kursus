			<h2>Pengertian Menjahit</h2>
		<p>Menjahit adalah pekerjaan menyambung kain, bulu, kulit binatang,
		atau bahan-bahan lain yang bisa dilewati jarum jahit dan benang. Menjahit
		dapat dilakukan dengan tangan memakai jarum tangan atau dengan mesin
		jahit (Wikipedia). Orang yang bekerja menjahit pakaian disebut dengan
		penjahit.<br> Dalam teknik jahit-menjahit benang dan jarum ditusuk ke kain untuk
		membuat berbagai bentuk jahitan sehingga dikenal berbagai jenis tusuk dan
		setik. Hasil dari menjahit dapat berupa pakaian, tirai, kasur, sprai, taplak, kain
		pelapis mebel dan kain pelapis jok. <br>Benda-benda lain yang dijahit dapat
		berupa layar, bendera, tenda, sepatu, tas dan sampul buku. Menjahit sebagian
		besar dilakukan memakai mesin jahit.<br><br></p>
		<h2>Pengelolaan Pelatihan</h2>
		<ol><li>Perencanaan
		<p>Perencanaan berkaitan dengan penyusunan tujuan dan rangkaian kegiatan untuk mencapai tujuan lembaga penyelenggara pendidikan luar sekolah.</p>

		<li>Pengorganisasian
		<p>Pengorganisasian adalah kegiatan mengidentifikasi dan memadukan
		sumber-sumber yang diperlukan kedalam kegiatan yang akan dilakukan
		dalam mencapai tujuan yang telah ditetapkan. Sumber-sumber itu meliputi
		tenaga manusia, fasilitas, alat-alat dan biaya yang tersedia atau dapat disedaiakan.</p>
		<li>Penggerakan
		<p>Penggerakan memainkan peranan yang sangat penting, disamping itu
		penggerakan berperanan pula dalam fungsi manajemen lainnya seperti
		pembinaan, penilaian dan pengembangan.<br> Fungsi penggerakan ialah untuk
		mewujudkan tingkat penampilan dan partisipasi yang tinggi dari setiap
		pelaksana yang terlibat dalam kegiatan untuk mencapai tujuan yang ditetapkan.</p>
		<li>Pengawasan
		<p>Fungsi manajemen lainnya adalah pembinaan. Didalamnya termasuk
		pengawasan, supervisi, dan monitoring. Pembinaan diselenggarakan
		melalui pendekatan langsung dan tidak langsung.<br> Pendekatan langsung
		dilakukan oleh pengelola dilakukan terhadap para penyelenggara dan
		pelaksana program atau kegiatan pendidikan luar sekolah. Pendekatan
		tidak langsung dilakukan melaui staf atau pihak lain berkaitan dengan tugas para penyelenggara dan pelaksana.</p>
		<li>Penilaian
